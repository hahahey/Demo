package main.gson;

public class DateInfo {
    private String zjId;
    private String jd;
    private String wd;
    private String sj;
    private String fx;
    private String sd;

    public String getZjId() {
        return zjId;
    }

    public void setZjId(String zjid) {
        this.zjId = zjid;
    }

    public String getJd() {
        return jd;
    }

    public void setJd(String jd) {
        this.jd = jd;
    }

    public String getWd() {
        return wd;
    }

    public void setWd(String wd) {
        this.wd = wd;
    }

    public String getSj() {
        return sj;
    }

    public void setSj(String sj) {
        this.sj = sj;
    }

    public String getFx() {
        return fx;
    }

    public void setFx(String fx) {
        this.fx = fx;
    }

    public String getSd() {
        return sd;
    }

    public void setSd(String sd) {
        this.sd = sd;
    }
}
